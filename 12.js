// == vs ===


let num1 = "7";
let num2 = 7;


console.log(num1 == num2); // == only checks the value not the data type
console.log(num1 === num2); // checks datatype also


// != vs !==
console.log(num1 != num2); // == only checks the value not the data type
console.log(num1 !== num2); // === checks datatype also





